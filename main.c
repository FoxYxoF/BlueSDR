// Функция перевода амплитуды Q15 в значение для пикселя водопада (0-255)
uint8_t magnitude_to_db_pixel(q15_t mag) {
    if (mag <= 0) return 0;

    // Вариант А: Использование стандартной библиотеки (точнее, но медленнее)
    // Переводим в float (0..1), берем лог, нормируем под экран
    float float_mag = (float)mag / 32767.0f;
    float db = 20.0f * log10f(float_mag + 1e-6f); // +1e-6 чтобы не было log(0)
    
    // Обычно диапазон водопада — это последние 60-80 дБ
    // Сдвигаем и масштабируем результат в 0..255 для отрисовки
    int16_t pixel = (int16_t)(db + 80) * 3; // Пример: -80дБ -> 0, 0дБ -> 240
    
    if (pixel < 0) return 0;
    if (pixel > 255) return 255;
    return (uint8_t)pixel;
}

